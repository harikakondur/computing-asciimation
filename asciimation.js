function animateText(text, elementId, textType) {
    let index = 0;
    let currentText = '';
    let element = document.createElement(textType);
    elementId.appendChild(element);

    return new Promise(resolve => {
        function addLetter() {
            currentText += text[index];
            element.innerHTML = currentText;
            index++;
            if (index < text.length) {
                setTimeout(addLetter, 100); // Typing speed
            } else {
                resolve();
            }
        }

        addLetter();
    });
}

let adaIndex = 0;

// Function to display a toothbrush
function displayAda(ada_lovelace) {
    document.getElementById('frames').innerHTML = ada_lovelace;
}


async function welcome() {
    let element = document.getElementById('welcome');
    console.log("welcome",element)
    await animateText("women in computing : an asciimation timeline", element, 'h1');
    // console.log('done')
    // element.appendChild(document.createElement('br')); 
    await animateText(".           by harika kondur", element, 'h3');

    // Delay the removal of the element
    await new Promise(resolve => setTimeout(resolve, 2000)); 
    element.remove();
} 

async function startAnimation() {
    let framesDiv = document.createElement('div');
    framesDiv.id = 'frames';
    framesDiv.style.display = 'flex'; // Set the display to flex

    let asciiDiv = document.createElement('div');
    asciiDiv.id = 'ascii'; 
    framesDiv.appendChild(asciiDiv);

    let captionDiv = document.createElement('div');
    console.log(captionDiv)
    captionDiv.id = 'caption';
    framesDiv.appendChild(captionDiv);


    let pre = document.createElement('pre');
    pre.appendChild(framesDiv);

    document.body.appendChild(pre);

    // Display the first frame
    displayAda(ada_lovelace[adaIndex], asciiDiv, captionDiv);

    // Set up the interval to display each frame
    setInterval(() => {
        adaIndex++;
        if (adaIndex >= ada_lovelace.length) {
            adaIndex = 0; // reset the index to loop back to the first frame
        }
        displayAda(ada_lovelace[adaIndex], asciiDiv, captionDiv);
    }, 500); // 500 milliseconds = 0.5 seconds

    captionDiv.innerText = "ada lovelace- the first computer programmer";
}

async function start() {
    await welcome();
    await startAnimation();
}

start();